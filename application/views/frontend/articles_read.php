                Dia mengatakan, sejak beberapa tahun lalu, semangat anak muda (milenial) untuk menjadi entrepreneur
                sudah marak terjadi, kemudian kini berkembang keinginannya untuk dapat menembus pasar ekspor.
                Dia menilai, anak muda mampu melihat peluang baru dan out of the box (kreatif) dalam mengembangkan
                produk ekspor.
                </p>
                <p>
                "Bukan hanya sektor-sektor yang mainstream yang dikembangkan seperti kopi atau porang, anak muda juga
                pintar mengembangkan produk-produk unik misalnya ada yang serius garap sabut kelapa, pelepah pisang
                dan lain-lain. Sekilas itu barang-barang yang sederhana atau bahkan sering dianggap sampah, tapi di
                tangan anak muda yang kreatif semua bisa jadi produk yang sangat bernilai ekonomis," kata Jerry dalam
                keterangan tertulis yang diterima, Minggu (3/10/2021).
