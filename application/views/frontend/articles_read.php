                Wakil Menteri Perdagangan (Wamendag) Jerry Sambuaga mengaku bangga dan menyambut baik terhadap
                fenomena gairah anak muda untuk bisa menjadi pelaku ekspor. Hal itu dikatakannya saat menerima
                Ketua Gerakan Muda Pembaharu Indonesia Petrus Sihombing dan beberapa pengurusnya di Kantor Kementerian
                Perdagangan beberapa hari lalu. Baca artikel detikfinance, "Milenial Lirik Bisnis Orientasi Ekspor, Apa Langkah Pemerintah?"
